#include "libft.h"

int		ft_intlen_base(int n, int b)
{
	int	len;

	len = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		len++;
		n /= b;
	}
	return (len);
}
